# config.py — настройки проекта CoreLine
# Переменные окружения для веб-поиска и MCP

import os

# Загружаем .env из корня проекта (LM_STUDIO_API_KEY и др.)
try:
    from dotenv import load_dotenv
    load_dotenv()
except ImportError:
    pass

# -----------------------------------------------------------------------------
# LM Studio API (чат и эмбеддинги)
# -----------------------------------------------------------------------------
# Если в LM Studio включено Require Authentication, задай токен:
#   export LM_STUDIO_API_KEY="твой-токен-из-lm-studio"
# или
#   export OPENAI_API_KEY="твой-токен-из-lm-studio"
# Токен задаётся/смотрится в LM Studio в настройках сервера (Authentication).
# -----------------------------------------------------------------------------

# -----------------------------------------------------------------------------
# Веб-поиск / MCP
# -----------------------------------------------------------------------------
# Поиск в интернете: через MCP-инструменты в LM Studio (по умолчанию).
# Команды *won / *woff включают и выключают доступ модели к поиску (как раньше).
#
# WEB_SEARCH_VIA_MCP=1 (по умолчанию) — модель использует инструменты MCP,
#   которые подключены к твоему LM Studio. Запрос уходит в LM Studio,
#   инструменты выполняются на стороне LM Studio.
#
# Чтобы подключить MCP-сервер к LM Studio: в LM Studio открой настройки
#   сервера и добавь свой MCP server (stdio или SSE). После этого при *won
#   модель будет получать подсказку о доступе к поиску и вызывать инструменты.
# -----------------------------------------------------------------------------

WEB_SEARCH_VIA_MCP = os.getenv("WEB_SEARCH_VIA_MCP", "1").lower() in ("1", "true", "yes")

# Текст системной подсказки при включённом *won (можно переопределить через env)
WEB_SEARCH_SYSTEM_PROMPT = os.getenv(
    "WEB_SEARCH_SYSTEM_PROMPT",
    "У тебя есть доступ к поиску в интернете через инструменты (MCP). "
    "Используй их, когда нужна актуальная информация, новости или данные, которых нет в твоих знаниях."
).strip()

# ID MCP-плагина в LM Studio (ключ из mcp.json → mcpServers). Задаётся в api.py через MCP_PLUGIN_ID (по умолчанию "web-search").
# Для будущего: MCP_SERVER_URL — если проект будет сам вызывать MCP по SSE.
MCP_SERVER_URL = os.getenv("MCP_SERVER_URL", "").strip()
